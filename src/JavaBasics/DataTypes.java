package JavaBasics;

public class DataTypes {

	//Main method --> It is the starting of execution Point of the program
	
	public static void main(String[] args) {
	
		//1.integer
		
		int i =10;
		i=20;
		//10 is replaced by 20 here.If you print 'i' value it will display 20.
		int k=34;
		int l=23;
		System.out.println(i);
		System.out.println(l+k);
		
		//At a time you can store a single value in variable
		
		//2.double
		double d=12.33;
		double d1=100;//integers can be stored as double
		System.out.println(d1);
		System.out.println(d+i);
		
		//3.character
		char c='a';//should be single letters and should be written within single quotes
		char c2='@';
		System.out.println(c2);
		
		//4.boolean
		boolean b1=true;
		boolean b2=false;
		//int,char,double,boolean are primitive datatypes
		//5.String
		
		//String is a class not datatype but we can use this as a datatype
		String s1="Soumya";
		String s2="Ranjan";
		String s3="1000";
		String s4="This my sentence";
		System.out.println(s4);
		//You can write anything in the double quotes
				
		
		
		
	}

}
